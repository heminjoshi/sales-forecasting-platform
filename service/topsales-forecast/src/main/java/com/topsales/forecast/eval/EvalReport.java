package com.topsales.forecast.eval;

import com.topsales.common.domain.Channel;

import java.math.BigDecimal;
import java.math.RoundingMode;
import java.util.Locale;

/**
 * Deterministic rendering of an {@link EvalResult} to three forms: a console table, the committed
 * markdown report ({@code docs/forecast-eval-report.md}), and a machine-diffable CSV
 * ({@code data/eval/forecast-eval.csv}).
 *
 * <p>Determinism is enforced by construction: the segment list arrives in a fixed total order; every
 * number is rounded to {@value #SCALE} dp {@code HALF_UP}; all formatting uses {@code Locale.ROOT};
 * and lines are joined with {@code '\n'} (never the platform separator). Two runs on the same seed
 * therefore produce byte-identical files.
 */
public final class EvalReport {

    private static final int SCALE = 4;
    private static final String NL = "\n";
    private static final String NA = "n/a";

    /** 4dp HALF_UP, Locale.ROOT; {@code "n/a"} for an undefined (NaN) metric. */
    static String num(double v) {
        if (Double.isNaN(v)) {
            return NA;
        }
        return BigDecimal.valueOf(v).setScale(SCALE, RoundingMode.HALF_UP).toPlainString();
    }

    private static String chan(Channel c) {
        return c.name();
    }

    // ---------------------------------------------------------------- markdown

    public String markdown(EvalResult r) {
        StringBuilder sb = new StringBuilder();
        sb.append("# Forecast evaluation report").append(NL).append(NL);
        sb.append(
                        "Regenerated by `make eval` (`com.topsales.forecast.eval.EvalMain`) — a"
                                + " deterministic expanding-window backtest on the committed seed. Do not"
                                + " hand-edit.")
                .append(NL)
                .append(NL);
        sb.append("- **Window:** ")
                .append(r.windowStart())
                .append(" … ")
                .append(r.windowEnd())
                .append(" (fixed, ")
                .append(java.time.temporal.ChronoUnit.DAYS.between(r.windowStart(), r.windowEnd()) + 1)
                .append(" days)")
                .append(NL);
        CvConfig cv = r.cv();
        sb.append("- **CV:** expanding-window, initialTrain=")
                .append(cv.initialTrainDays())
                .append("d, testHorizon=")
                .append(cv.testHorizonDays())
                .append("d, step=")
                .append(cv.stepDays())
                .append("d, maxFolds=")
                .append(cv.maxFolds())
                .append(" → ")
                .append(r.foldCount())
                .append(" folds")
                .append(NL);
        sb.append(
                        "- **Metrics:** WAPE = Σ|a−f|/Σ|a|, bias = Σ(f−a)/Σ|a| — pooled (ratio-of-sums,"
                                + " volume-weighted). No-sale days align as real zeros; a Σ|a|=0 segment is"
                                + " `defined=false` and excluded from the rollup.")
                .append(NL)
                .append(NL);

        // Section A
        sb.append("## Section A — Per-segment metrics").append(NL).append(NL);
        sb.append("| forecaster | tenant | category | channel | n | WAPE | bias | defined |").append(NL);
        sb.append("|---|---|---|---|---:|---:|---:|---|").append(NL);
        for (SegmentMetrics m : r.segments()) {
            sb.append("| ")
                    .append(m.model())
                    .append(" | ")
                    .append(m.key().tenantId())
                    .append(" | ")
                    .append(m.key().categoryId())
                    .append(" | ")
                    .append(chan(m.key().channel()))
                    .append(" | ")
                    .append(m.n())
                    .append(" | ")
                    .append(num(m.wape()))
                    .append(" | ")
                    .append(num(m.bias()))
                    .append(" | ")
                    .append(m.defined())
                    .append(" |")
                    .append(NL);
        }
        sb.append(NL);

        // Section B
        sb.append("## Section B — SeasonalNaive vs HoltWinters (per series)").append(NL).append(NL);
        sb.append(
                        "HoltWinters is expected to win on dense, seasonal series; both degrade on the"
                                + " sparse `cat_collectibles`.")
                .append(NL)
                .append(NL);
        sb.append("| tenant | category | channel | naive WAPE | HW WAPE | winner |").append(NL);
        sb.append("|---|---|---|---:|---:|---|").append(NL);
        for (EvalResult.Comparison c : r.comparisons()) {
            sb.append("| ")
                    .append(c.key().tenantId())
                    .append(" | ")
                    .append(c.key().categoryId())
                    .append(" | ")
                    .append(chan(c.key().channel()))
                    .append(" | ")
                    .append(c.naive() == null ? NA : num(c.naive().wape()))
                    .append(" | ")
                    .append(c.holtWinters() == null ? NA : num(c.holtWinters().wape()))
                    .append(" | ")
                    .append(c.winner())
                    .append(" |")
                    .append(NL);
        }
        sb.append(NL);

        // Section C
        sb.append("## Section C — Overall pooled rollup").append(NL).append(NL);
        sb.append("| forecaster | segments | n | WAPE | bias |").append(NL);
        sb.append("|---|---:|---:|---:|---:|").append(NL);
        for (EvalResult.PooledMetrics p : r.overall()) {
            sb.append("| ")
                    .append(p.model())
                    .append(" | ")
                    .append(p.segments())
                    .append(" | ")
                    .append(p.points())
                    .append(" | ")
                    .append(num(p.wape()))
                    .append(" | ")
                    .append(num(p.bias()))
                    .append(" |")
                    .append(NL);
        }
        sb.append(NL);
        return sb.toString();
    }

    // --------------------------------------------------------------------- csv

    public String csv(EvalResult r) {
        StringBuilder sb = new StringBuilder();
        sb.append("grain,forecaster,tenant,category,channel,n,wape,bias,defined").append(NL);
        for (SegmentMetrics m : r.segments()) {
            sb.append("segment,")
                    .append(m.model())
                    .append(',')
                    .append(m.key().tenantId())
                    .append(',')
                    .append(m.key().categoryId())
                    .append(',')
                    .append(chan(m.key().channel()))
                    .append(',')
                    .append(m.n())
                    .append(',')
                    .append(num(m.wape()))
                    .append(',')
                    .append(num(m.bias()))
                    .append(',')
                    .append(m.defined())
                    .append(NL);
        }
        for (EvalResult.PooledMetrics p : r.overall()) {
            sb.append("overall,")
                    .append(p.model())
                    .append(",,,,")
                    .append(p.points())
                    .append(',')
                    .append(num(p.wape()))
                    .append(',')
                    .append(num(p.bias()))
                    .append(",true")
                    .append(NL);
        }
        return sb.toString();
    }

    // ----------------------------------------------------------------- console

    public String console(EvalResult r) {
        StringBuilder sb = new StringBuilder();
        sb.append("WAPE backtest — ")
                .append(r.windowStart())
                .append(" … ")
                .append(r.windowEnd())
                .append(" — ")
                .append(r.foldCount())
                .append(" folds")
                .append(NL);
        sb.append(line("model", "tenant", "category", "channel", "n", "WAPE", "bias")).append(NL);
        for (SegmentMetrics m : r.segments()) {
            sb.append(
                            line(
                                    m.model(),
                                    m.key().tenantId(),
                                    m.key().categoryId(),
                                    chan(m.key().channel()),
                                    String.valueOf(m.n()),
                                    num(m.wape()),
                                    num(m.bias())))
                    .append(NL);
        }
        sb.append(NL).append("Overall pooled:").append(NL);
        for (EvalResult.PooledMetrics p : r.overall()) {
            sb.append(
                            String.format(
                                    Locale.ROOT,
                                    "  %-14s segments=%-3d n=%-5d WAPE=%s bias=%s",
                                    p.model(),
                                    p.segments(),
                                    p.points(),
                                    num(p.wape()),
                                    num(p.bias())))
                    .append(NL);
        }
        return sb.toString();
    }

    private static String line(
            String model,
            String tenant,
            String category,
            String channel,
            String n,
            String wape,
            String bias) {
        return String.format(
                Locale.ROOT,
                "  %-14s %-8s %-16s %-8s %5s %9s %9s",
                model,
                tenant,
                category,
                channel,
                n,
                wape,
                bias);
    }
}
